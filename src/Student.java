public class Student {
    //멤버 변수 선언
    private String student_id;
    private String student_name;
    private String student_account;
    private String student_major;
    private String student_graduate;
    private String student_bus;
    private String student_domitory;
    private String student_ethaddr;
    private String student_pw;

    public String getStudent_id(){
        return student_id;
    }
    public void setStudent_id(String student_id)
    {
        this.student_id = student_id;
    }
    public String getStudent_name(){
        return student_name;
    }
    public void setStudent_name(String student_name){
        this.student_name = student_name;
    }
    public String getStudent_account(){
        return student_account;
    }
    public void setStudent_account(String student_account){
        this.student_account = student_account;
    }
    public String getStudent_major(){
        return student_major;
    }
    public void setStudent_major(String student_major){
        this.student_major = student_major;
    }
    public String getStudent_graduate(){
        return student_graduate;
    }
    public void setStudent_graduate(String student_graduate){
        this.student_graduate = student_graduate;
    }
    public String getStudent_bus(){
        return student_bus;
    }
    public void setStudent_bus(String student_bus){
        this.student_bus = student_bus;
    }
    public String getStudent_domitory(){
        return student_domitory;
    }
    public void setStudent_domitory(String student_domitory){
        this.student_domitory = student_domitory;
    }
    public String getStudent_ethaddr(){
        return student_ethaddr;
    }
    public void setStudent_ethaddr(String student_ethaddr){
        this.student_ethaddr = student_ethaddr;
    }
    public String getStudent_pw(){
        return student_pw;
    }
    public void setStudent_pw(String student_pw){
        this.student_pw = student_pw;
    }


}
